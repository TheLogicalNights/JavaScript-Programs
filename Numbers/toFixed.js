// toFixed function takes floating number as input and rounds it off to given position

var iNo = 87.356875;

console.log("......Before toFixed......");
console.log(iNo);

iNo1 = iNo.toFixed();
iNo2 = iNo.toFixed(2);
iNo3 = iNo.toFixed(3);
iNo4 = iNo.toFixed(4);

console.log("......After toFixed......");
console.log(iNo1);
console.log(iNo2);
console.log(iNo3);
console.log(iNo4);